package Quiz2.class2;

public class FourTransactions {
    public int total(int x, int y){
        return x + y;
    }
    public int exract(int x, int y){
        return x - y;
    }
    public int multiply(int x, int y){
        return x * y;
    }
    public int divide(int x, int y){
        return x / y;
    }
}
